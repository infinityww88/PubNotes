from typing import List

class Solution:
    def check(self, board, i, j, val):
        for k in range(9):
            if board[i][k] == val:
                return False
        for k in range(9):
            if board[k][j] == val:
                return False
        i = i // 3 * 3
        j = j // 3 * 3
        for k in range(3):
            for t in range(3):
                if board[i+k][j+t] == val:
                    return False
        return True

    def _solveSudoku(self, board: List[List[str]]) -> bool:
        for i in range(9):
            for j in range(9):
                if board[i][j] == '.':
                    for k in "123456789":
                        if self.check(board, i, j, k):
                            board[i][j] = k
                            if self._solveSudoku(board):
                                return True
                            board[i][j] = '.'
                    return False
        return True
    def solveSudoku(self, board: List[List[str]]) -> None:
        self._solveSudoku(board)

